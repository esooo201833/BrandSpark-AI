export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.en;

export function getTranslation(
  language: Language | string,
  key: TranslationKey
): string {
  const validLanguages = Object.keys(translations) as Language[];
  const lang = (validLanguages.includes(language as Language) ? language : 'en') as Language;
  const translation = translations[lang];
  if (translation && key in translation) {
    return translation[key as keyof typeof translation] as string;
  }
  return (translations.en[key as keyof typeof translations.en] as string) || key;
}
